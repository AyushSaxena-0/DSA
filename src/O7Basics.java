public class O7Basics {
    public static void main(String[] args){
        int a=10;
        //if condition
        if(a==10){
            System.out.println("Hello World !");
        }
        //loop
        int count=0;
        while(count<10){
            System.out.println("While loop");
            count++;
        }
        System.out.println();
        //for loop
        for(int i=0;i<10;i++){
            System.out.println("For loop");
        }
    }
}
